package vihuela.data;

public class Page {
  private int pageId;
  private String filename;
  
  public int getPageId() {
    return pageId;
  }
  public void setPageId(int pageId) {
    this.pageId = pageId;
  }
  public String getFilename() {
    return filename;
  }
  public void setFilename(String filename) {
    this.filename = filename;
  }  
}
